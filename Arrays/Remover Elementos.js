// Como remover elementos de um Array

const numeros = [1,2,3,5,6,7,8,9]

//Início (Shift)

numeros.shift()

// Meio (Splice)

numeros.splice(Posição,Deletados)

// Posição(Posição onde deve ser Deletado elemento.)
// Deletados(Quantidade de itens q deve se deletar após o indice definido em Posição.)

// Final (Pop)

numeros.pop()
