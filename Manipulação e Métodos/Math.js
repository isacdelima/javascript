// Uso do Método Math

Math.PI // Retorna o valor PI
// ex. Math.PI = 3.145...

Math.sqrt(numero) // Retorna Raiz quadrada de 'numero'
// ex:Math.sqrt(9) = 3

Math.abs(numero) // Retorna 'numero' absoluto
// ex: Math.abs(-50) = 50

Math.round(numero) // Arredonda o número para o valor inteiro mais próximo.
// ex: Math.round(5.6)=6

Math.floor(numero) // Arredonda o numero para 'baixo'
// ex: Math.floor(5.8) = 5

Math.max(numero) // Retorna o maior de zero ou mais numeros
// ex:Math.max(5,9,1,4,7)=1

Math.min(numero) // Retorna o menor de zero ou mais numeros
// ex:Math.max(5,9,1,4,7)=9

Math.random(numero) // Retorna um número aleatório entre 0 e 1.
// Caso o parâmetro seja preenchido, retorna um número entre os inseridos.
// ex1:Math.random()= 0.489498
// ex2:Math.random(1,10)=3,4189

Math.sign(numero) // Retorna 1, negativo ou positivo, de acordo com o sinal do num inserido.
// ex1=Math.sign(15200)=1
// ex2=Math.sign(-65156)=-1

